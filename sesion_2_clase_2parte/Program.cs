// Operadores de asignación
/* Operator  Example    Same as
*    =	      x = 5	     x = 5	
*   -=	      x -= 3	   x = x - 3	
*   +=	      x += 3	   x = x + 3	
*   *=	      x *= 3	   x = x * 3	
*   /=	      x /= 3	   x = x / 3	
*   %=	      x %= 3	   x = x % 3	
*   &=	      x &= 3	   x = x & 3	
*   |=	      x |= 3	   x = x | 3	
*   ^=	      x ^= 3	   x = x ^ 3	
*   >>=	      x >>= 3	   x = x >> 3	
*   <<=	      x <<= 3	   x = x << 3
*/

